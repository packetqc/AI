/* ── Public API ─────────────────────────────────────────────────────────── */

const char *LLM_TokenStr(int32_t token_id)
{
    if (token_id < 0 || token_id >= LLM_VOCAB_SIZE) return "?";
    return llm_vocab[token_id];
}

int LLM_Tokenize(const char *text, int32_t *ids, int32_t *mask, int max_len)
{
    int n = 0;

    /* Step 1: byte → initial token ID */
    for (const char *p = text; *p && n < max_len; p++) {
        unsigned char b = (unsigned char)*p;
        if (b == 0x20) {
            ids[n++] = 221;         /* space → Ġ token */
        } else if (b >= 33 && b <= 126) {
            ids[n++] = (int32_t)(b - 32);  /* printable ASCII */
        }
        /* Non-ASCII bytes skipped — model trained on ASCII grammar */
    }

    /* Step 2: apply BPE merges in priority order */
    int changed = 1;
    while (changed && n > 1) {
        changed = 0;
        int best_m   = LLM_MERGES_NUM; /* lowest merge index wins */
        int best_pos = -1;

        for (int i = 0; i < n - 1; i++) {
            int16_t a = (int16_t)ids[i];
            int16_t b = (int16_t)ids[i + 1];
            for (int m = 0; m < best_m; m++) {
                if (llm_merges[m][0] == a && llm_merges[m][1] == b) {
                    best_m   = m;
                    best_pos = i;
                    break;
                }
            }
        }

        if (best_pos >= 0) {
            ids[best_pos] = (int32_t)llm_merges[best_m][2];
            for (int j = best_pos + 1; j < n - 1; j++)
                ids[j] = ids[j + 1];
            n--;
            changed = 1;
        }
    }

    /* Step 3: pad remainder and fill mask */
    int real_len = (n < max_len) ? n : max_len;
    for (int i = real_len; i < max_len; i++)
        ids[i] = 0;
    for (int i = 0; i < max_len; i++)
        mask[i] = (i < real_len) ? 1 : 0;

    return real_len;
}
